import numpy as np

# Hyperbolic Functions

x = np.sinh(np.pi/2)

print(x)

# Another example

arr = np.array([np.pi/2, np.pi/3, np.pi/4, np.pi/5])

x = np.cosh(arr)

print(x)

# Finding Angles

x = np.arcsinh(1.0)

print(x)

# Angles of Each Value in Arrays

arr = np.array([0.1, 0.2, 0.5])

x = np.arctanh(arr)

print(x)




